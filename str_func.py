def convert_str(string):
    """Возвращает все буквы в словах заглавными"""
    upper_letters_str = string.upper()
    return upper_letters_str
def convert_first_letter(string):
    """Возвращает слова с заглавными первыми буквами"""
    first_letter_str = string.title()
    return first_letter_str
